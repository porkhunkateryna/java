package cw3;

public class t2_3 {
	public static String chartobinary(char ch){
        return Integer.toBinaryString(ch);
    }
	public static void main(String[] args) {
	
	   char h ='h';
	   char e ='e';
	   char l ='l';
	   char l2 ='l';
	   char o ='o';
		 
	   System.out.println(chartobinary(h));
	   System.out.println(chartobinary(e));
	   System.out.println(chartobinary(l));
	   System.out.println(chartobinary(l2));
	   System.out.println(chartobinary(o));
		
	}

		

}
